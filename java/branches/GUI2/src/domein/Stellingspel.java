package domein;

import java.util.List;

public class Stellingspel extends Onderdeel {
	private List<Stelling> stellingen;
	
	public Stellingspel(){
		super();
		this.setType(SoortOnderdeel.STELLINGSPEL);
	}
	
	public void insertStelling(Stelling stelling){
		stellingen.add(stelling);
	}

	public List<Stelling> getStellingen() {
		return stellingen;
	}

	public void setStellingen(List<Stelling> stellingen) {
		this.stellingen = stellingen;
	}



	
	
	

}

package domein;

public class StellingAntwoord {
	String antwoord;
	StellingAntwoordKleur kleur;
	
	public StellingAntwoord(String antwoord, StellingAntwoordKleur kleur){
		setAntwoord(antwoord);
		setKleur(kleur);
	}
	
	public String getAntwoord() {
		return antwoord;
	}
	public void setAntwoord(String antwoord) {
		this.antwoord = antwoord;
	}
	public StellingAntwoordKleur getKleur() {
		return kleur;
	}
	public void setKleur(StellingAntwoordKleur kleur) {
		this.kleur = kleur;
	}
	
	

}
